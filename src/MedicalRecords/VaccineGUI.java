    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        if (pid.getText().equals("") || vn.getText().equals("") || nod.getText().equals("") || nob.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "All fields are required!");
        } else if (PatientIdExist()) {
            JOptionPane.showMessageDialog(null, "Patient Id didn't Exist!");
        } else {

            try {

                vaccineRecord vacInfo = new vaccineRecord();

                vacInfo.setPatientID(Integer.parseInt(pid.getText()));
                vacInfo.setvName(vn.getText());
                vacInfo.setNumDoses(Integer.parseInt(nod.getText()));
                vacInfo.setNumBoosters(Integer.parseInt(nob.getText()));
                allRecord.add(vacInfo);

                SaveAllVData();
                pid.setText("");
                vn.setText("");
                nod.setText("");
                nob.setText("");
            } catch (Exception e) {
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_saveActionPerformed

