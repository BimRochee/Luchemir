    Patient getPData(int cid) {
        for (int i = 0; i < allPatients.size(); i++) {
            if (allPatients.get(i).getId() == cid) {
                return allPatients.get(i);
            }
        }
        return null;
    }

    vaccineRecord getRData(int cid) {
        for (int i = 0; i < allRecord.size(); i++) {
            if (allRecord.get(i).getPatientID() == cid) {
                return allRecord.get(i);
            }
        }
        return null;
    }

    void readAllVData() {
        try {
            File pfile = new File("vdata.txt");
            Scanner scan = new Scanner(pfile);

            while (scan.hasNextLine()) {
                String data = scan.nextLine();
                String[] curData = data.split(";");
                vaccineRecord record = new vaccineRecord();
                record.setPatientID(Integer.parseInt(curData[0]));

                record.setvName(curData[1]);

                record.setNumDoses(Integer.parseInt(curData[2]));
                record.setNumBoosters(Integer.parseInt(curData[3]));

                allRecord.add(record);
            }

            scan.close();

        } catch (Exception e) {

        }
    }

    void readAllPData() {
        try {
            File pfile = new File("pdata.txt");
            Scanner scan = new Scanner(pfile);

            while (scan.hasNextLine()) {
                String data = scan.nextLine();
                String[] curData = data.split(";");
                Patient patient = new Patient();
                patient.setId(Integer.parseInt(curData[0]));

                patient.setName(curData[1]);

                patient.setAge(Integer.parseInt(curData[2]));

                patient.setGender(curData[3]);

                patient.setAddress(curData[4]);

                patient.setContact(curData[5]);

                allPatients.add(patient);

            }

            scan.close();

        } catch (Exception e) {

        }
    }

    boolean PatientIdExist() {

        for (int i = 0; i < allPatients.size(); i++) {
            if (pid.getText().equals(allPatients.get(i).getId() + "")) {
                return false;
            }
        }

        return true;
    }

    void SaveAllVData() {
        try {
            FileWriter fileWriter = new FileWriter("vdata.txt");

            for (int i = 0; i < allRecord.size(); i++) {
                fileWriter.append(allRecord.get(i).getPatientID() + ";" + allRecord.get(i).getvName() + ";" + allRecord.get(i).getNumDoses() + ";" + allRecord.get(i).getNumBoosters() + ";");
                int tmp = allRecord.get(i).getPatientID();
                fileWriter.append(tmp + ",");

                fileWriter.append("\n");
            }

            fileWriter.close();
        } catch (Exception e) {

        }
    }

