    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        if (pid.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Id field is empty!");
        } else {
            for (int i = 0; i < allRecord.size(); i++) {
                if (allRecord.get(i).getPatientID() == Integer.parseInt(pid.getText())) {
                    vax.append("\n\nPatient Info\n\n");
                    Patient patient = getPData(allRecord.get(i).getPatientID());
                    vaccineRecord vrec = getRData(allRecord.get(i).getPatientID());

                    vax.append("Patient Name: " + patient.getName() + "\n");
                    vax.append("Vaccine Name: " + vrec.getvName() + "\n");
                    vax.append("Number of Doses: " + vrec.getNumDoses() + "\n");
                    vax.append("Number of Boosters: " + vrec.getNumBoosters() + "\n");

                    vax.append("\n********************************************************************************************************************************************\n");
                }
            }
        }
    }//GEN-LAST:event_searchActionPerformed

